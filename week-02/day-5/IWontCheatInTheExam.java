package loops;

public class IWontCheatInTheExam {
    public static void main(String[] args) {
        // Írj egy programot ami 100-szor kiírja:
        // "Nem fogok csalni a vizsgán!"
        int i = 0;
        while (i < 100) {
            System.out.println("\"Nem fogok csalni a vizsgán!\"");
            i++;
        }
    }
}